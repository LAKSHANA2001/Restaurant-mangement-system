    private void foodIdFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_foodIdFieldActionPerformed
        // TODO add your handling code here:
        try
        {
        String orderId=orderField.getText();
        String foodId=foodIdField.getText();
        Connection con=ConnectionProvider.getCon();
        Statement st=con.createStatement();
        String avail="Yes";
        ResultSet rs=st.executeQuery("select * from food where food_id='"+foodId+"' and available='"+avail+"'");
        if(rs.next())
        {
            foodNameField.setText(rs.getString(2));
            priceField.setText(rs.getString(4));
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_foodIdFieldActionPerformed

