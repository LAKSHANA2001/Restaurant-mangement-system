    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        String orderId=orderField.getText();
        String foodId=foodIdField.getText();
        String foodName=foodNameField.getText();
        String price=priceField.getText();
        
        String quantity=quantityField.getText();
        String totalPrice=totalPriceField.getText();
        try
        {
        Connection con=ConnectionProvider.getCon();
        Statement st=con.createStatement();
        st.executeUpdate("insert into order_place values('"+orderId+"','"+foodId+"','"+foodName+"','"+price+"','"+quantity+"','"+totalPrice+"')");
        setVisible(false);
        new userDasboard().setVisible(true);
        JOptionPane.showMessageDialog(null,"Successfully added");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_addBtnActionPerformed

